public class Ship {
    public String model;
    public String size;

    public Ship(String model, String size) {
        this.model = model;
        this.size = size;

    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }


    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}

