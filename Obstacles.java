public class Obstacles {
    private int damage;
    private String type;

    public Obstacles(int damage, String type) {
        this.damage = damage;
        this.type = type;
    }

    public int getDamage() {
        return damage;
    }
    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }
}



